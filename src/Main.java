    private void btEntraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntraActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost/cadastro";
        try {            
            Connection conexao = (Connection) DriverManager.getConnection(url,"root","");
            PreparedStatement pesquisa = conexao.prepareStatement("SELECT id,nome,senha,nascimento,sexo FROM pessoa");
            ResultSet resultado = pesquisa.executeQuery();
            String nome;
            String senha;
            String dNome;
            String dSenha;
            limit();
            int contador = 0;           
            while(resultado.next()){
                contador++;
                nome = resultado.getString("nome");
                senha = resultado.getString("senha");
                dNome = getNome.getText();
                dSenha = getSenha.getText();                 
                if(nome.equals(dNome) && senha.equals(dSenha)){                    
                    id = resultado.getInt("id");
                    identifica.setId(id);
                    identifica.setNome(nome);
                    String data = resultado.getString("nascimento");
                    identifica.setData(data);
                    String sex = resultado.getString("sexo");
                    identifica.setSexo(sex);
                    tela.setVisible(true);
                    dispose();                                       
                    break;                    
                }else if(limitador == contador){
                    if(!nome.equals(dNome) && !senha.equals(dSenha)){
                        JOptionPane.showMessageDialog(null,"Senha ou Login Inv√°lido!");                        
                    }
                }
            }                        
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btEntraActionPerformed

