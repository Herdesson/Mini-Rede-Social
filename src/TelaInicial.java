    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        // TODO add your handling code here:
        Identificador logo = new Identificador();        
        String url = "jdbc:mysql://localhost/cadastro";
        String novoNome = editNome.getText();
        String novaSenha = editSenha.getText();
        int ano = (int) editAno.getValue();
        int mes = (int) editMes.getValue();
        int dia = (int) editDia.getValue();
        int ident = logo.getId();        
        ImageIcon image = new ImageIcon(setIcon);
        imageIcon.setIcon(image);
        String novoNascimento = ano+"-"+mes+"-"+dia;
        String update = "UPDATE pessoa SET nome='"+novoNome+"',senha='"+novaSenha+"',nascimento='"+novoNascimento+"',sexo='"+sexo+"' WHERE id="+ident+"";        
        try{
            Connection conect = (Connection) DriverManager.getConnection(url, "root", "");
            PreparedStatement atualiza = conect.prepareStatement(update);
            atualiza.executeUpdate();
            JOptionPane.showMessageDialog(null,"Credenciais alteradas com sucesso");
        }catch(Exception erro){
        }
    }//GEN-LAST:event_editarActionPerformed

