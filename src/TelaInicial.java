    public void getUpload(){
        Identificador logo = new Identificador();       
        JFileChooser fc = new JFileChooser();        
        int res = fc.showOpenDialog(null);
        if(res == JFileChooser.APPROVE_OPTION){
            File arquivo = fc.getSelectedFile();
            try {               
                setIcon = ImageIO.read(arquivo);                
                File setCaminho = new File("src/Imagem/"+arquivo.getName());
                while(setCaminho.exists() != false){
                    int numerador = 1;
                    File novCaminho = new File("src/Imagem"+arquivo.getName()+numerador);
                    numerador++;
                }
                Files.copy(Paths.get(arquivo.getAbsolutePath()), Paths.get(setCaminho.getPath()), COPY_ATTRIBUTES); 
                caminho = "Imagem>"+arquivo.getName();
                ImageIcon image = new ImageIcon(setIcon); 
                image.setImage(image.getImage().getScaledInstance(imageIcon.getWidth(), imageIcon.getHeight(), 1));
                imageIcon.setIcon(image);
            } catch (IOException ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }    
    public void getUploadPerfil(){
        Identificador ident = new Identificador();
        
        int ide = ident.getId();
        String url = "jdbc:mysql://localhost/cadastro";
        String sql = "SELECT caminho FROM pessoa WHERE id="+ide;
        try{
            Connection conect = (Connection) DriverManager.getConnection(url,"root","");
            PreparedStatement pesquisa = conect.prepareStatement(sql);
            ResultSet resultado = pesquisa.executeQuery();
            while(resultado.next()){
                String cam = resultado.getString("caminho");
                cam = cam.replaceAll(">", "/");                
                getIcon = new ImageIcon(getClass().getResource(cam));
                getIcon.setImage(getIcon.getImage().getScaledInstance(imageIcon.getWidth(), imageIcon.getHeight(), 1));
                imageIcon.setIcon(getIcon);
                break;
            }
        }catch(Exception e){
        }
    }
